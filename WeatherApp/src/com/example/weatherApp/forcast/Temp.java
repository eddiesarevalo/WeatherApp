package com.example.weatherApp.forcast;

public class Temp {
	public double day;
	public double min;
	public double max;
	public double night;
	public double eve;
	public double morn;
	public Temp(double day, double min, double max, double night, double eve,
			double morn) {
		this.day = day;
		this.min = min;
		this.max = max;
		this.night = night;
		this.eve = eve;
		this.morn = morn;
	}
	
}
